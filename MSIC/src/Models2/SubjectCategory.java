package Models2;

public class SubjectCategory {
    public int id_categorie_disciplina;
    public String denumire;
    public int prioritate;

    public SubjectCategory(int id_categorie_disciplina, String denumire, int prioritate) {
        this.id_categorie_disciplina = id_categorie_disciplina;
        this.denumire = denumire;
        this.prioritate = prioritate;
    }
}
